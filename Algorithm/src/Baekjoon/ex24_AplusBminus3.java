package Baekjoon;

import java.util.Scanner;

public class ex24_AplusBminus3 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int T = sc.nextInt(); // 테스트 케이스 개수

		for (int i = 0; i < T; i++) {

			int num1 = sc.nextInt();
			int num2 = sc.nextInt();
			
			System.out.println(num1 + num2);
		}

	}

}
